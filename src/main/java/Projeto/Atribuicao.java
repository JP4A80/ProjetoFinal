public class Atribuicao {
    private String disiplina;
    private String iddisplina;
    private String professor;
    private String idprofessor;
    
    public Atribuicao(String disiplina, String iddisplina, String professor, String idprofessor){
        super();
        this.disiplina = disiplina;
        this.iddisplina = iddisplina;
        this.professor = professor;
        this.idprofessor = idprofessor;
    }

    public String getDisiplina() {
        return disiplina;
    }

    public void setDisiplina(String disiplina) {
        this.disiplina = disiplina;
    }

    public String getIddisplina() {
        return iddisplina;
    }

    public void setIddisplina(String iddisplina) {
        this.iddisplina = iddisplina;
    }

    public String getProfessor() {
        return professor;
    }

    public void setProfessor(String professor) {
        this.professor = professor;
    }

    public String getIdprofessor() {
        return idprofessor;
    }

    public void setIdprofessor(String idprofessor) {
        this.idprofessor = idprofessor;
    }
    
    
    
    
    
    
    
    
}
